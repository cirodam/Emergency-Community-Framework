        <h1>The Benjamin Franklin Society</h1>
        <p class="subtitle">A member-governed community, built on the model of mutual aid.</p>
    </div>

    <div class="section">
        <p>
            In 1727, Benjamin Franklin gathered a small group of tradespeople in Philadelphia and
            proposed that they meet regularly — to share what they knew, help each other with
            problems, and work on things that would improve the life of their city. They called it
            the Junto. It gave rise to the first public library in America, the first volunteer fire
            department, and a hospital. None of it came from a government or a wealthy patron. It
            came from neighbors who decided to build something together.
        </p>
        <p>
            The Benjamin Franklin Society takes that as its model. Not as nostalgia, but as a
            working method. Communities that organize themselves — that run their own institutions,
            look after their own members, and hold their own governance — don't need to wait for
            outside help. They already have what they need: each other.
        </p>
    </div>

    <hr class="divider" />

    <div class="charter">
        <h2 class="charter-title">Charter</h2>

        <div class="article">
            <h3>Article I — What a Benjamin Franklin Society Is</h3>
            <p>
                A Benjamin Franklin Society is a member-governed community organization. It exists
                to meet the needs of its members — not to grow, not to generate returns for outside
                investors, not to serve any interest other than the people who belong to it.
            </p>
            <p>
                Every society is independent. It writes its own bylaws, admits its own members,
                makes its own decisions. No outside authority — not an association, not the Society
                at large, not the founders of this software — governs the internal life of a
                society. That authority belongs to the members.
            </p>
            <p>
                A Benjamin Franklin Society is not a political organization. It does not take
                positions on elections or candidates. It is open to people of any background or
                political view who are willing to work alongside their neighbors and abide by the
                society's own rules.
            </p>
        </div>

        <div class="article">
            <h3>Article II — What Every Society Commits To</h3>
            <p>
                By adopting this charter, a society makes the following commitments to its members.
                These are not subject to local amendment. They are the floor below which a society
                cannot go and still be a Benjamin Franklin Society.
            </p>
            <ul class="commitments">
                <li>
                    <strong>We will look after our members.</strong>
                    Every member receives basic needs support unconditionally. Support is not
                    withheld as punishment. Membership is not contingent on health, productivity,
                    or economic contribution.
                </li>
                <li>
                    <strong>We will govern ourselves fairly.</strong>
                    Every member has an equal standing in the society's governance. Assembly
                    members are drawn by sortition — random selection from the eligible
                    membership — so no individual or faction can acquire a controlling position
                    by campaigning, wealth, or influence. Decisions are made by the members,
                    not made for them.
                </li>
                <li>
                    <strong>We will treat members with due process.</strong>
                    No member's standing may be suspended, revoked, or materially restricted
                    without notice, a stated reason, and a genuine opportunity to respond
                    before the decision takes effect.
                </li>
                <li>
                    <strong>We will provide a right of appeal.</strong>
                    Any member subject to an adverse governance decision may appeal it to an
                    independent body before it takes permanent effect.
                </li>
                <li>
                    <strong>We will not punish people retroactively.</strong>
                    No rule change may be applied to penalize conduct that was permitted when
                    it occurred.
                </li>
                <li>
                    <strong>We will keep an honest ledger.</strong>
                    All exchange flows within the society are visible to all members.
                    No parallel books.
                </li>
                <li>
                    <strong>We will not surveil, manipulate, or coerce our members.</strong>
                    Member data belongs to members. The society does not sell or share member
                    information with outside parties. It does not use advertising, algorithmic
                    pressure, or social incentives to push members toward particular views or
                    behaviors. Members are entitled to their own opinions. The society does
                    not require conformity — only that members treat each other with basic
                    respect and abide by the rules they agreed to.
                </li>
                <li>
                    <strong>We will let members leave freely.</strong>
                    Members may take their own records and go at any time. A society may not
                    hold a member's data or balance hostage to continued membership.
                </li>
            </ul>
        </div>

        <div class="article">
            <h3>Article III — Required Institutions</h3>
            <p>
                The commitments in Article II are not aspirations. They are operational
                requirements. A society that sincerely intends to keep them must build the
                institutions that make keeping them possible. The following institutions are
                non-negotiable — every Benjamin Franklin Society must maintain them.
            </p>

            <div class="institution">
                <h4>A Community Bank</h4>
                <p>
                    Every society operates a community bank that issues and manages the society's
                    internal currency. The bank is not owned by any individual member or faction.
                    Its ledger is open to all members. It does not lend money it does not have,
                    does not charge interest on mutual-aid accounts, and does not operate parallel
                    books. The bank exists to keep exchange flowing — not to accumulate.
                </p>
                <p>
                    The community currency is designed to circulate, not to be hoarded. It carries
                    a demurrage charge — a small periodic fee on idle balances — so that kin held
                    without use gradually returns to the community pool. This is not a penalty. It
                    is the mechanism that keeps the currency working as a medium of exchange rather
                    than a store of private wealth.
                </p>
            </div>

            <div class="institution">
                <h4>A Social Insurance Fund</h4>
                <p>
                    Every society maintains a social insurance fund. All members contribute to it
                    according to their means. All members may draw from it according to their need.
                    The fund covers basic necessities — food, shelter, medical care, and the other
                    things without which a person cannot live with dignity.
                </p>
                <p>
                    Access to the fund is not contingent on a member's economic productivity,
                    health status, or history of contribution. A member who falls ill, loses work,
                    ages out of employment, or faces an emergency is entitled to support. This is
                    what the commitment to look after members actually means in practice.
                </p>
                <p>
                    The fund is governed by the membership, not administered unilaterally by any
                    officer. Its balance and disbursements are visible to all members.
                </p>
            </div>

            <div class="institution">
                <h4>A Governing Assembly</h4>
                <p>
                    Every society operates a governing assembly drawn by sortition — members
                    selected at random from the eligible membership, not by election. Sortition
                    prevents the community assembly from being captured by a faction that campaigns well.
                    A randomly-drawn body represents the community as it actually is, not as its
                    most organised members would prefer it to be.
                </p>
                <p>
                    The community assembly sets policy, amends bylaws, draws or removes officers, and makes
                    decisions that cannot be delegated. No executive, no committee, and no officer
                    may act in ways the community assembly has not authorised or would not authorise if
                    asked. The community assembly does not need to meet continuously, but it must meet
                    regularly and must be the final authority on the society's direction.
                </p>
            </div>
        </div>

        <div class="article">
            <h3>Article IV — Relationship to Other Societies</h3>
            <p>
                Societies may join together into associations — groups of societies that trade and
                look after each other across a wider area. Joining an association is voluntary.
                A society that joins an association does not hand over its governance to it.
                The association serves the societies; the societies do not serve the association.
            </p>
            <p>
                Societies dealing with each other deal honestly. Exchange records are accurate and
                not manipulated. A society that manipulates its records or defrauds other societies
                has broken the terms on which cooperation rests — other societies are under no
                obligation to continue associating with it.
            </p>
            <p>
                A society may stop participating in the wider Benjamin Franklin Society at any time
                by member vote. Its own records, its exchange ledger, and its members' data remain
                its own. Any open balances with other societies are settled and the connection is
                closed.
            </p>
        </div>

        <div class="article">
            <h3>Article V — What Makes a Benjamin Franklin Society</h3>
            <p>
                This charter does not govern communities. Communities govern themselves. What this
                charter does is define what the name means.
            </p>
            <p>
                A Benjamin Franklin Society is a community that upholds the commitments in
                Article II and operates the Benjamin Franklin Society software with those
                commitments active and enforced — including the institutions required by
                Article III. That's it. No application, no approval, no paperwork required.
            </p>
            <p>
                A community that does not uphold the commitments in Article II is not a Benjamin
                Franklin Society. It may be a fine community. It may do good work. But the name
                belongs to the definition, and the definition requires those commitments. A
                community that abandons them has not been expelled — it has simply become
                something else.
            </p>
        </div>

        <p class="charter-footer">
            This charter defines what the Benjamin Franklin Society is. It is maintained by the
            founding societies and amended by agreement among established societies.
        </p>
    </div>
</div>

