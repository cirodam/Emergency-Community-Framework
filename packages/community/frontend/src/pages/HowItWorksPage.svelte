        <h1>You haven't been abandoned.</h1>
        <p class="subtitle">The people around you still need each other.</p>
    </div>

    <div class="section">
        <h2>What's happening</h2>
        <p>
            The broader economy has failed a lot of people. Jobs, savings, institutions — things
            that were supposed to be dependable have turned out not to be. If you're here, you
            already know that.
        </p>
        <p>
            This isn't a bank. It's not a government. It's not a corporation with a terms-of-service
            you'll never read. It's a coordination tool for people who have decided to take care
            of each other.
        </p>
        <p>
            No interest. No exploitation.
        </p>
    </div>

    <div class="section">
        <h2>What backs it</h2>
        <p>
            Not gold. Not debt. Not a promise from someone far away.
        </p>
        <p>
            What backs this community is the people in it — their labor, their skills, their
            willingness to show up. A farmer who feeds people. A nurse who helps them heal.
            A carpenter who fixes roofs. A neighbor who watches children.
            Demographics and shared desire to survive are the most durable backing there is.
        </p>
    </div>

    <div class="section">
        <h2>Where this idea comes from</h2>
        <p>
            During the Great Depression, when formal institutions collapsed, communities survived
            through mutual aid societies — fraternal organizations, neighborhood networks, church
            groups — where members contributed what they could and drew on it when they needed to.
        </p>
        <p>
            This is the same idea, built for now. Formal enough to be fair. Flexible enough to
            work in a crisis.
        </p>
    </div>

    <div class="section">
        <h2>How it works</h2>
        <div class="steps">
            <div class="step">
                <div class="step-num">1</div>
                <div class="step-text">
                    <strong>Members vouch for each other.</strong>
                    Someone already in the community sponsors you. Trust is extended person to person,
                    not issued by a central authority.
                </div>
            </div>
            <div class="step">
                <div class="step-num">2</div>
                <div class="step-text">
                    <strong>Contribute what you can.</strong>
                    Skills, time, goods — the community tracks what flows in and out so that
                    cooperation stays fair over time.
                </div>
            </div>
            <div class="step">
                <div class="step-num">3</div>
                <div class="step-text">
                    <strong>Govern together — democratically.</strong>
                    The community sets its own rules through its constitution. No one person
                    owns this. It belongs to everyone in it. Decisions are made collectively,
                    not handed down by politicians or administrators. Everyone is sick of that.
                </div>
            </div>
        </div>
    </div>

    <hr class="divider" />

    <div class="section">
        <h2>You're not going to get rich here</h2>
        <p>
            This is not an investment opportunity. There is no token to speculate on.
            The currency in this community is designed to stay in this community — it
            flows between members in exchange for real things: food, work, care, time.
        </p>
        <p>
            Hoarding it doesn't help you. There's nothing to redeem it for outside these
            walls, and there's no interest to collect. The point is circulation — the same
            kin that pays your neighbor's rent this month might come back to you as
            food next month.
        </p>
        <p>
            If you're looking to build personal wealth, this is the wrong place. If you're
            looking to survive with dignity alongside people who are doing the same — you're
            in the right place.
        </p>
    </div>

    <hr class="divider" />

